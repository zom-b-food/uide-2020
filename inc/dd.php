						work
						primarily with
						front-end frameworks such as Angular, React, Backbone and so forth and are
						less
						concerned
						with the visual aspects of a page such as padding, font sizes, image
						responsiveness,
						content
						flow and positioning. UI devs manage DOM objects like image, button, form,
						etc;
						whereas
						front-end devs manage javascript objects such as function, array, string,
						etc.
						UI/UX
						creators are page builders and create user flows, wireframes, clickable
						prototypes,
						style
						guides, and page-level objects and behaviors as well; they also make a web
						page
						responsive
						for a legion of modern devices.</p>

                <h2>"UI/UX" vs "Front-end" </h2>

                <p>UI/UX creators are primarily concerned with the "look and feel" of a page/view
                    and work within the DOM to style a page according to project specifications.
                    Front-end developers deal with the data aspects at the page-level. They work
                    primarily with front-end frameworks such as Angular, React, Backbone and so
                    forth and are less concerned with the visual aspects of a page such as padding,
                    font sizes, image responsiveness, content flow and positioning. UI devs manage
                    DOM objects like image, button, form, etc; whereas front-end devs manage
                    javascript objects such as function, array, string, etc. UI/UX creators are page
                    builders and create user flows, wireframes, clickable prototypes, style guides,
                    and page-level objects and behaviors as well; they also make a web page
                    responsive for a legion of modern devices. Front-end developers nowadays write
                    javascript. Period. And as a good friend of mine told me recently, "the
                    front-end world has become a raging tire fire". As a Sr level UI/UX Designer
                    &amp; Developer I am proficient in UI skills/frameworks such as Bootstrap,
                    Foundation, Materialize, SASS, Javascript, AJAX, REST, JQuery, JSP, PHP, HTML5,
                    Grunt/Gulp, ACS, usability scripting, middle-tier integration (.NET, RoR, and
                    JSP), and Opencart and Wordpress experience. So if you find yourself smelling rubber, please
                    give me a call and we'll put out the fire together.</p> <div id="summary">&nbsp;</div>
